const dictionaries = { en, de };

const persisted = (() => {
  try {
    const value = localStorage.getItem('scan2target_lang');
    return value === 'de' || value === 'en' ? value : 'en';
  } catch {
    return 'en';
  }
})();

export const lang = writable(persisted);

lang.subscribe((value) => {
  try {
    localStorage.setItem('scan2target_lang', value);
  } catch {
    // localStorage unavailable (private mode etc.)
  }
});

function interpolate(template, vars) {
  if (!vars) return template;
  return template.replace(/\{(\w+)\}/g, (m, key) => (vars[key] !== undefined ? String(vars[key]) : m));
}

export const t = derived(lang, ($lang) => {
  const dict = dictionaries[$lang] || en;
  return (key, vars) => {
    const value = dict[key] ?? en[key];
    if (value === undefined) {
      // Unknown key (e.g. an unexpected backend status): show something readable.
      return key.startsWith('status_') ? key.slice('status_'.length) : key;
    }
    return interpolate(value, vars);
  };
});
